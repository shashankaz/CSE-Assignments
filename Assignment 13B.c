//Write a program in C to separate the individual characters from a string without using library functions.

#include <stdio.h>

int main() 
{
    char str[100];
    int i;

    printf("Enter a String : ");
    scanf("%s", str);

    printf("The Characters in the String are :\n");

    for (i = 0; str[i] != '\0'; i++) 
    {
        printf("%c ", str[i]);
    }

    printf("\n");

    return 0;
}
